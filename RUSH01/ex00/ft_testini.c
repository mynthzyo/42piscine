#include <unistd.h>

int	ft_testini(char *argv[])
{
	int	i;

	i = 0;
	while (argv[1][i] != '\0')
	{
		if (((argv[1][i] - 48) < 1 || (argv[1][i] - 48) > 4))
		{
			if (argv[1][i] != 32)
			{
				write(2, "Error\n", 6);
				return (1);
			}
		}
		i++;
	}
	if (i != 31)
	{
		write (2, "Error\n", 6);
		return (1);
	}
	return (0);
}
